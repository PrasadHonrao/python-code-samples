# integer
intValue = 10
print("Integer : {0}".format(intValue))

# binary representation has to start with 0b
binaryValue = 0b10 
print("Binary : {0}".format(binaryValue))

# octal representation has to start with 0o
octalValue = 0o10 
print("Octal : {0}".format(octalValue))

# hex representation has to start with 0x
hexValue = 0x10 
print("Hex : {0}".format(hexValue))

# int constructor
ten = int(10) 
print("int constructor : {}".format(ten))

# string to int conversion
stringInt = int("123") 
print("string to int conversion : {}".format(stringInt))

# float to int conversion
floatInt = int(123456.12) 
print("float to int conversion : {}".format(floatInt))