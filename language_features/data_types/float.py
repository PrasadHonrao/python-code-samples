# float declaration
floatValue = 3.14159
print("float value : {0}".format(floatValue))

# constructor
floatValue = float(10.99)
print("float constructor : {0}".format(floatValue))

# string to float conversion
floatValue = float("10.11")
print("string to float conversion : {0}".format(floatValue))

# creating infinity using float constructor
print(float("inf"))
print(float("-inf"))

# any operation which involves float will result into float
print(3.1 + 4) 