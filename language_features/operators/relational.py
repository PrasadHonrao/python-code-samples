#  == equal to 
#  != not equal to 
#  > greater than 
#  < less then
#  >= greater than or equal to
#  <= less than or equal to

first = 10
second = 20

print(str(first) + " == " + str(second) + '? : ', first == second)
print(str(first) + " != " + str(second) + '? : ', first != second)
print(str(first) + " > " + str(second) + '? : ', first > second)
print(str(first) + " < " + str(second) + '? : ', first < second)
print(str(first) + " >= " + str(second) + '? : ', first >= second)
print(str(first) + " <= " + str(second) + '? : ', first <= second)

# string comparison
print('carl' < 'chris')
print('carl' <= 'carl')

# cross data type comparison
print( 3 < 4.1)